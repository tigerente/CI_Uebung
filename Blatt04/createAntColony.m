function antColony = createAntColony(nrOfIndivids)
%CREATEANTCOLONY(nrOfIndivids)
% Erstellt eine Ameisenkolonie
% PARAMETERS:
%   nrOfIndivids:       Anzahl der Ameisen
%
% RETURN:
%   antColony:          Cell-Matrix mit 'nrOfIndivids' Zeilen. In jede Cell
%                       wird spaeter der Pfad geschrieben, den die Ameise
%                       laeuft

antColony = cell(nrOfIndivids,1);

end
